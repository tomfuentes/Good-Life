package com.goodlife.model;

public class ObjectPair {

	private Object objR;
	private Object objL;

	public ObjectPair() {
		super();
	}

	public ObjectPair(Object objR, Object objL) {
		super();
		this.objR = objR;
		this.objL = objL;
	}

	public Object getObjR() {
		return objR;
	}

	public void setObjR(Object objR) {
		this.objR = objR;
	}

	public Object getObjL() {
		return objL;
	}

	public void setObjL(Object objL) {
		this.objL = objL;
	}
	
}
